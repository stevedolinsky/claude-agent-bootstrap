\`\`\`

## Code Patterns
$(case "$LANG_TYPE" in
  node)
    echo "- Source directory: \`$SRC_DIR/\`"
    [[ -d "$SRC_DIR/lib" ]] && echo "- Shared utilities: \`$SRC_DIR/lib/\`"
    [[ -d "$SRC_DIR/components" ]] && echo "- Components: \`$SRC_DIR/components/\`"
    [[ -d "$SRC_DIR/app" ]] && echo "- App Router pages: \`$SRC_DIR/app/\`"
    [[ -d "$SRC_DIR/pages" ]] && echo "- Pages: \`$SRC_DIR/pages/\`"
    [[ -f "$SRC_DIR/lib/types.ts" ]] && echo "- Types: \`$SRC_DIR/lib/types.ts\`"
    ;;
  python)
    echo "- Source directory: \`$SRC_DIR/\`"
    [[ -d "tests" ]] && echo "- Tests: \`tests/\`"
    [[ -f "pyproject.toml" ]] && echo "- Config: \`pyproject.toml\`"
    ;;
  go)
    echo "- Source: \`./\` (Go modules)"
    echo "- Tests: \`*_test.go\` files alongside source"
    echo "- Config: \`go.mod\`"
    ;;
  rust)
    echo "- Source: \`src/\`"
    echo "- Tests: \`tests/\` directory and inline \`#[cfg(test)]\`"
    echo "- Config: \`Cargo.toml\`"
