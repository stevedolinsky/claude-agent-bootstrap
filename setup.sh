wait_for_idle() {
  local session="$1"
  local timeout="${2:-120}"
  local elapsed=0

  while [[ $elapsed -lt $timeout ]]; do
    local pane_text
    pane_text=$(tmux capture-pane -t "$session" -p -S -30 2>/dev/null | sed 's/\x1b\[[0-9;]*[a-zA-Z]//g' | tr -d '\r' || echo "")

    # Claude Code shows "❯" prompt when ready for input
    if echo "$pane_text" | grep -q '❯'; then
      sleep 2
      return 0
    fi

    sleep 3
    elapsed=$((elapsed + 3))
