# --- Helper: send a prompt file to a tmux session ---
send_loop() {
  local session="$1"
  local file="$2"
  local name
  name=$(basename "$file" .txt)

  tmux load-buffer -b loop-cmd "$file"
  tmux paste-buffer -b loop-cmd -t "$session"
  tmux send-keys -t "$session" Enter
  echo "   + $name"
  sleep 3
}

