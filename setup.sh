    # Use tmux wait-for + remain-on-exit to detect when the worker finishes.
    # This lets us expire the dedup key immediately instead of a fixed 30m timer.
    window_id = f'worker-{key[-1]}-{threading.get_ident() % 10000}'
    spawn = subprocess.run([
        'tmux', 'new-window', '-d', '-t', session,
        '-n', window_id,
        '-PF', '#{pane_id}',
        f'{cmd}; tmux wait-for -S {window_id}',
    ], capture_output=True, text=True)
    if spawn.returncode == 0:
        logging.info(f'Spawned {model} in {session}')
        log_event('spawned', event_type=t, number=num, title=title,
                  model=model, session=session, worker_id=window_id)
        def _wait_and_expire():
            spawn_time = time.time()
            subprocess.run(['tmux', 'wait-for', window_id], timeout=3600,
                           capture_output=True)
            duration = round(time.time() - spawn_time, 1)
            if t == 'issue':
                time.sleep(60)  # cooldown: prevent rapid re-dispatch for same issue
            with _in_flight_lock:
                _in_flight.discard(key)
            log_event('done', event_type=t, number=num, model=model,
                      session=session, worker_id=window_id,
                      detail=f'duration_seconds={duration}')
            logging.info(f'Worker done, released {key} ({duration}s)')
        threading.Thread(target=_wait_and_expire, daemon=True).start()
    else:
        logging.error(f'Failed to open tmux window in {session} — is the session running?')
        log_event('error', event_type=t, number=num, model=model,
                  session=session, detail='tmux-spawn-failed')
        with _in_flight_lock:
            _in_flight.discard(key)


