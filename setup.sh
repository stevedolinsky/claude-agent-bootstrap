#!/usr/bin/env bash
#
# Claude Agent Bootstrap v3 — any repo, any language, any model setup.
# Usage: ~/claude-agent-bootstrap/setup.sh [--defaults]
#
# Run from your project root. Generates:
#   - CLAUDE.md (agent instructions, appended to existing)
#   - LOOPS.md (loop documentation + manual paste commands)
#   - .claude/settings.json (autonomous permissions)
#   - .claude/loops/*.txt (individual loop prompt files)
#   - .claude/bootstrap.conf (saved config)
#   - start-loops.sh (tmux launcher)
#   - .github/workflows/ci.yml (if applicable)
#
# Supports: Node.js, Python, Go, Rust, and generic projects
# Models: Dual (Sonnet+Opus), Single Sonnet, or Single Opus
#
set -euo pipefail

REPO_ROOT="$(git rev-parse --show-toplevel 2>/dev/null || pwd)"
cd "$REPO_ROOT"
# ─── Parse arguments ─────────────────────────────────────────────────
USE_DEFAULTS=false
for arg in "$@"; do
  case "$arg" in
    --defaults) USE_DEFAULTS=true ;;
  esac
done

# ─── Detect project ──────────────────────────────────────────────────
REPO_NAME=$(basename "$REPO_ROOT")
ORIGIN_URL=$(git remote get-url origin 2>/dev/null || echo "")
GITHUB_USER=$(echo "$ORIGIN_URL" | sed -n 's|.*github.com[:/]\([^/]*\)/.*|\1|p')
GITHUB_REPO=$(echo "$ORIGIN_URL" | sed -n 's|.*github.com[:/][^/]*/\(.*\)\.git|\1|p')

if [[ -z "${GITHUB_REPO:-}" ]]; then
  GITHUB_REPO=$(echo "$ORIGIN_URL" | sed -n 's|.*github.com[:/][^/]*/\(.*\)|\1|p')
fi

echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "  Claude Agent Bootstrap v3"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""
echo "Project: $REPO_NAME"
echo "GitHub:  ${GITHUB_USER:-unknown}/${GITHUB_REPO:-unknown}"

if [[ -z "${GITHUB_USER:-}" ]] || [[ -z "${GITHUB_REPO:-}" ]]; then
  echo ""
  echo "WARNING: Could not detect GitHub remote. Agent loops need it."
  echo "  Set up with: git remote add origin https://github.com/<user>/<repo>.git"
  echo "  Then re-run this script."
fi
# ─── Interactive config ──────────────────────────────────────────────
BOOTSTRAP_CONF=".claude/bootstrap.conf"
mkdir -p .claude

if [[ -f "$BOOTSTRAP_CONF" ]]; then
  echo ""
  echo "Using saved config from $BOOTSTRAP_CONF"
  source "$BOOTSTRAP_CONF"
elif [[ "$USE_DEFAULTS" == "true" ]] || [[ ! -t 0 ]]; then
  MODEL_MODE="single-sonnet"
  AUTO_LABELS="true"
  LOOP_SPEED="normal"
  echo ""
  echo "Using defaults: single-sonnet, auto-labels, normal speed"
else
  echo ""
  echo "? Model mode:"
  echo "  [1] Dual (Sonnet+Opus) — 2 sessions, 7 loops"
  echo "  [2] Sonnet only — 1 session, 5 loops (recommended to start)"
  echo "  [3] Opus only — 1 session, 5 loops"
  read -rp "  Choice [2]: " mode_choice
  case "${mode_choice:-2}" in
    1) MODEL_MODE="dual" ;;
    3) MODEL_MODE="single-opus" ;;
    *) MODEL_MODE="single-sonnet" ;;
  esac

  read -rp "? Auto-create GitHub labels? [Y/n]: " label_choice
  case "${label_choice:-Y}" in
    [nN]*) AUTO_LABELS="false" ;;
    *) AUTO_LABELS="true" ;;
  esac

  echo "? Loop speed:"
  echo "  [1] Fast (2m/5m) — aggressive, higher API cost"
  echo "  [2] Normal (5m/15m) — balanced"
  echo "  [3] Relaxed (10m/30m) — conservative, lower cost"
  read -rp "  Choice [2]: " speed_choice
  case "${speed_choice:-2}" in
    1) LOOP_SPEED="fast" ;;
    3) LOOP_SPEED="relaxed" ;;
    *) LOOP_SPEED="normal" ;;
  esac
fi

# Save config
cat > "$BOOTSTRAP_CONF" << CONF_EOF
# Claude Agent Bootstrap config — generated by setup.sh v3
MODEL_MODE="$MODEL_MODE"
AUTO_LABELS="$AUTO_LABELS"
LOOP_SPEED="$LOOP_SPEED"
CONF_EOF

echo "  Mode: $MODEL_MODE | Labels: $AUTO_LABELS | Speed: $LOOP_SPEED"

# ─── Map speed to intervals ─────────────────────────────────────────
case "$LOOP_SPEED" in
  fast)
    TRIAGE_INTERVAL="2m"; WORKER_INTERVAL="5m"; RESPONDER_INTERVAL="2m"
    MAINTENANCE_INTERVAL="30m"; SWEEP_INTERVAL="1h"
    ;;
  relaxed)
    TRIAGE_INTERVAL="10m"; WORKER_INTERVAL="30m"; RESPONDER_INTERVAL="10m"
    MAINTENANCE_INTERVAL="2h"; SWEEP_INTERVAL="4h"
    ;;
  *) # normal
    TRIAGE_INTERVAL="5m"; WORKER_INTERVAL="15m"; RESPONDER_INTERVAL="5m"
    MAINTENANCE_INTERVAL="1h"; SWEEP_INTERVAL="2h"
    ;;
esac
# ─── Detect agent mode (Tailscale push vs queue-branch polling) ───────
AGENT_MODE="queue"
TS_HOSTNAME=""
TS_TAG="${TS_TAG:-}"  # may be pre-set from sourced bootstrap.conf

if command -v tailscale &>/dev/null && tailscale status &>/dev/null 2>&1; then
  AGENT_MODE="tailscale"
  TS_HOSTNAME=$(tailscale status --json 2>/dev/null \
    | python3 -c "import json,sys; d=json.load(sys.stdin); print(d.get('Self',{}).get('DNSName','').rstrip('.'))" 2>/dev/null \
    || echo "")
  echo ""
  echo "✓ Tailscale detected — using hybrid mode (webhook-driven + maintenance polling)"
  echo "  Machine: ${TS_HOSTNAME:-<not detected — run: tailscale status>}"
  TS_TAG="${TS_TAG:-ci}"
  echo "  Tag: tag:$TS_TAG  (change: delete .claude/bootstrap.conf and re-run, or edit workflows)"
  # Persist TS_TAG to conf for re-runs (avoids re-prompting)
  if [[ -f "$BOOTSTRAP_CONF" ]] && ! grep -q "^TS_TAG=" "$BOOTSTRAP_CONF" 2>/dev/null; then
    echo "TS_TAG=\"$TS_TAG\"" >> "$BOOTSTRAP_CONF"
  fi
else
  TS_TAG="${TS_TAG:-ci}"
  echo ""
  echo "! Tailscale not detected — using queue-branch polling mode"
fi

# Persist AGENT_MODE to conf for re-runs
if [[ -f "$BOOTSTRAP_CONF" ]]; then
  if grep -q "^AGENT_MODE=" "$BOOTSTRAP_CONF" 2>/dev/null; then
    sed -i "s|^AGENT_MODE=.*|AGENT_MODE=\"$AGENT_MODE\"|" "$BOOTSTRAP_CONF"
  else
    echo "AGENT_MODE=\"$AGENT_MODE\"" >> "$BOOTSTRAP_CONF"
  fi
fi

# ─── Multi-language detection ────────────────────────────────────────
LANG_TYPE="generic"
PKG_DIR=""
PKG_MGR=""
SRC_DIR=""
INSTALL_CMD=""
LINT_CMD=""
LINT_FIX_CMD=""
BUILD_CMD=""
TEST_CMD=""

# Detect in priority order
if [[ -f "package.json" ]]; then LANG_TYPE="node"; PKG_DIR="."
elif [[ -f "web/package.json" ]]; then LANG_TYPE="node"; PKG_DIR="web"
elif [[ -f "app/package.json" ]]; then LANG_TYPE="node"; PKG_DIR="app"
elif [[ -f "frontend/package.json" ]]; then LANG_TYPE="node"; PKG_DIR="frontend"
elif [[ -f "pyproject.toml" ]] || [[ -f "requirements.txt" ]]; then LANG_TYPE="python"
elif [[ -f "go.mod" ]]; then LANG_TYPE="go"
elif [[ -f "Cargo.toml" ]]; then LANG_TYPE="rust"
fi

case "$LANG_TYPE" in
  node)
    # Package manager
    if [[ -f "$PKG_DIR/bun.lockb" ]]; then PKG_MGR="bun"
    elif [[ -f "$PKG_DIR/pnpm-lock.yaml" ]]; then PKG_MGR="pnpm"
    elif [[ -f "$PKG_DIR/yarn.lock" ]]; then PKG_MGR="yarn"
    else PKG_MGR="npm"; fi

    # Install
    if [[ "$PKG_MGR" == "npm" ]]; then INSTALL_CMD="cd $PKG_DIR && npm ci"
    else INSTALL_CMD="cd $PKG_DIR && $PKG_MGR install"; fi

    # Source directory
    if [[ -d "$PKG_DIR/src" ]]; then SRC_DIR="$PKG_DIR/src"
    elif [[ -d "$PKG_DIR/app" ]]; then SRC_DIR="$PKG_DIR/app"
    elif [[ -d "$PKG_DIR/lib" ]]; then SRC_DIR="$PKG_DIR/lib"
    else SRC_DIR="$PKG_DIR"; fi

    # Detect npm scripts (no python3 dependency)
    SCRIPTS=""
    if command -v node &>/dev/null; then
      SCRIPTS=$(node -e "try{const p=require('./$PKG_DIR/package.json');console.log(Object.keys(p.scripts||{}).join(' '))}catch(e){}" 2>/dev/null || echo "")
    fi
    if [[ -z "$SCRIPTS" ]]; then
      SCRIPTS=$(grep -oE '"(lint|build|test|dev|start|typecheck)"' "$PKG_DIR/package.json" 2>/dev/null | tr -d '"' | tr '\n' ' ' || echo "")
    fi

    [[ "$SCRIPTS" == *"lint"* ]] && LINT_CMD="cd $PKG_DIR && $PKG_MGR run lint"
    [[ "$SCRIPTS" == *"build"* ]] && BUILD_CMD="cd $PKG_DIR && $PKG_MGR run build"
    [[ "$SCRIPTS" == *"test"* ]] && TEST_CMD="cd $PKG_DIR && $PKG_MGR test"

    # Vitest fallback
    if [[ -z "$TEST_CMD" ]] && grep -q "vitest" "$PKG_DIR/package.json" 2>/dev/null; then
      TEST_CMD="cd $PKG_DIR && npx vitest run"
    fi

    # Lint fix
    if [[ -n "$LINT_CMD" ]]; then LINT_FIX_CMD="$LINT_CMD --fix"
    else LINT_FIX_CMD="cd $PKG_DIR && npx eslint ${SRC_DIR} --fix"; fi
    ;;

  python)
    if [[ -f "pyproject.toml" ]]; then INSTALL_CMD="pip install -e ."
    else INSTALL_CMD="pip install -r requirements.txt"; fi

    if [[ -d "src" ]]; then SRC_DIR="src"; else SRC_DIR="."; fi

    LINT_CMD="ruff check ."
    LINT_FIX_CMD="ruff check --fix ."
    TEST_CMD="pytest"
    ;;

  go)
    INSTALL_CMD="go mod download"
    SRC_DIR="."

    if [[ -f ".golangci.yml" ]] || [[ -f ".golangci.yaml" ]]; then
      LINT_CMD="golangci-lint run ./..."
      LINT_FIX_CMD="golangci-lint run --fix ./..."
    else
      LINT_CMD="go vet ./..."
      LINT_FIX_CMD=""
    fi

    BUILD_CMD="go build ./..."
    TEST_CMD="go test ./..."
    ;;

  rust)
    SRC_DIR="src"
    LINT_CMD="cargo clippy"
    LINT_FIX_CMD="cargo clippy --fix --allow-dirty"
    BUILD_CMD="cargo build"
    TEST_CMD="cargo test"
    ;;

  generic)
    SRC_DIR="."
    ;;
esac

echo ""
echo "Language: $LANG_TYPE"
[[ -n "$PKG_DIR" ]] && echo "Package dir: $PKG_DIR"
[[ -n "$PKG_MGR" ]] && echo "Package manager: $PKG_MGR"
echo "Source dir: ${SRC_DIR:-.}"
echo "Commands:"
echo "  install: ${INSTALL_CMD:-none}"
echo "  lint:    ${LINT_CMD:-none}"
echo "  build:   ${BUILD_CMD:-none}"
echo "  test:    ${TEST_CMD:-none}"

# ─── Build verify chain ──────────────────────────────────────────────
VERIFY_CHAIN=""
[[ -n "$INSTALL_CMD" ]] && VERIFY_CHAIN="$INSTALL_CMD"
[[ -n "$LINT_CMD" ]] && VERIFY_CHAIN="${VERIFY_CHAIN:+$VERIFY_CHAIN && }$LINT_CMD"
[[ -n "$BUILD_CMD" ]] && VERIFY_CHAIN="${VERIFY_CHAIN:+$VERIFY_CHAIN && }$BUILD_CMD"
[[ -n "$TEST_CMD" ]] && VERIFY_CHAIN="${VERIFY_CHAIN:+$VERIFY_CHAIN && }$TEST_CMD"

# ─── Model configuration ─────────────────────────────────────────────
SINGLE_MODEL=""
SINGLE_MODEL_NAME=""
case "$MODEL_MODE" in
  single-opus)  SINGLE_MODEL="claude-opus-4-6"; SINGLE_MODEL_NAME="Opus" ;;
  single-sonnet) SINGLE_MODEL="claude-sonnet-4-6"; SINGLE_MODEL_NAME="Sonnet" ;;
esac
# ─── MCP preflight check ─────────────────────────────────────────────
echo ""
MCP_OK=false
if [[ -f "$HOME/.claude.json" ]] && grep -q '"github"' "$HOME/.claude.json" 2>/dev/null; then
  MCP_OK=true
  echo "GitHub MCP server detected"
fi

PAT_OK=false
if [[ -n "${GITHUB_PERSONAL_ACCESS_TOKEN:-}" ]]; then
  PAT_OK=true
  echo "GitHub PAT detected"
else
  echo "WARNING: GITHUB_PERSONAL_ACCESS_TOKEN not set."
  echo "  Required for label creation and GitHub MCP server."
  echo "  export GITHUB_PERSONAL_ACCESS_TOKEN=ghp_..."
fi

if [[ "$MCP_OK" == "false" ]]; then
  echo "WARNING: GitHub MCP server not detected. The agent loops require it."
  echo "  Run this to configure it:"
  echo "    claude mcp add github -- npx -y @modelcontextprotocol/server-github"
  echo "  Then set GITHUB_PERSONAL_ACCESS_TOKEN in your environment."
  echo ""
  if [[ -t 0 ]] && [[ "$USE_DEFAULTS" == "false" ]]; then
    read -rp "  Continue anyway? [y/N]: " mcp_choice
    case "${mcp_choice:-N}" in
      [yY]*) echo "  Continuing (loops will fail without GitHub MCP)" ;;
      *) echo "  Aborting. Configure GitHub MCP and re-run."; exit 1 ;;
    esac
  else
    echo "  Continuing without GitHub MCP (configure before starting loops)"
  fi
fi

echo ""

# ─── Create .claude/settings.json ────────────────────────────────────
cat > .claude/settings.json << 'SETTINGS_EOF'
{
  "permissions": {
    "allow": [
      "Bash",
      "Edit",
      "Read",
      "Write",
      "Glob",
      "Grep",
      "Agent",
      "WebSearch",
      "WebFetch",
      "mcp__*"
    ],
    "deny": [
      "Bash(rm -rf /)*",
      "Bash(npm publish*)",
      "Bash(cargo publish*)",
      "Bash(pip upload*)",
      "Bash(twine upload*)"
    ]
  }
}
SETTINGS_EOF
echo "Created .claude/settings.json"
# ─── Create CI workflow ───────────────────────────────────────────────
if [[ -f ".github/workflows/ci.yml" ]]; then
  echo "ci.yml already exists, skipping"
elif [[ "$LANG_TYPE" == "generic" ]]; then
  echo "Skipping CI (generic project — configure manually if needed)"
else
  mkdir -p .github/workflows

  case "$LANG_TYPE" in
    node)
      # Detect cache settings
      CACHE_MGR="npm"
      CACHE_DEP_PATH="$PKG_DIR/package-lock.json"
      if [[ "$PKG_MGR" == "yarn" ]]; then
        CACHE_MGR="yarn"; CACHE_DEP_PATH="$PKG_DIR/yarn.lock"
      elif [[ "$PKG_MGR" == "pnpm" ]]; then
        CACHE_MGR="pnpm"; CACHE_DEP_PATH="$PKG_DIR/pnpm-lock.yaml"
      elif [[ "$PKG_MGR" == "bun" ]]; then
        CACHE_MGR=""; CACHE_DEP_PATH=""
      fi

      IS_NEXTJS=""
      if grep -q '"next"' "$PKG_DIR/package.json" 2>/dev/null; then
        IS_NEXTJS="true"
      fi

      # Build CI steps dynamically
      CI_STEPS=""
      [[ -n "$INSTALL_CMD" ]] && CI_STEPS="${CI_STEPS}      - run: $INSTALL_CMD
"
      [[ -n "$LINT_CMD" ]] && CI_STEPS="${CI_STEPS}      - run: $LINT_CMD
"
      if [[ -n "$BUILD_CMD" ]]; then
        if [[ -n "$IS_NEXTJS" ]]; then
          CI_STEPS="${CI_STEPS}      - run: $BUILD_CMD
        # Add NEXT_PUBLIC_* env vars if your build needs them:
        # env:
        #   NEXT_PUBLIC_API_URL: \${{ secrets.NEXT_PUBLIC_API_URL }}
"
        else
          CI_STEPS="${CI_STEPS}      - run: $BUILD_CMD
"
        fi
      fi
      if [[ -n "$TEST_CMD" ]]; then
        CI_STEPS="${CI_STEPS}      - name: Run tests
        run: $TEST_CMD
        continue-on-error: true
"
      fi

      cat > .github/workflows/ci.yml << CI_EOF
name: CI

on:
  push:
    branches: [main]
  pull_request:
    branches: [main]

concurrency:
  group: ci-\${{ github.ref }}
  cancel-in-progress: true

jobs:
  check:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-node@v4
        with:
          node-version: 20
$(if [[ -n "$CACHE_MGR" ]]; then
echo "          cache: $CACHE_MGR"
echo "          cache-dependency-path: $CACHE_DEP_PATH"
fi)
$CI_STEPS
CI_EOF
      ;;

    python)
      cat > .github/workflows/ci.yml << 'CI_EOF'
name: CI

on:
  push:
    branches: [main]
  pull_request:
    branches: [main]

concurrency:
  group: ci-${{ github.ref }}
  cancel-in-progress: true

jobs:
  check:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-python@v5
        with:
          python-version: '3.12'
          cache: 'pip'
CI_EOF
      # Append dynamic parts
      if [[ -f "pyproject.toml" ]]; then
        echo "      - run: pip install -e ." >> .github/workflows/ci.yml
      else
        echo "      - run: pip install -r requirements.txt" >> .github/workflows/ci.yml
      fi
      echo "      - run: ruff check ." >> .github/workflows/ci.yml
      cat >> .github/workflows/ci.yml << 'CI_EOF'
      - name: Run tests
        run: pytest
        continue-on-error: true
CI_EOF
      ;;

    go)
      cat > .github/workflows/ci.yml << 'CI_EOF'
name: CI

on:
  push:
    branches: [main]
  pull_request:
    branches: [main]

concurrency:
  group: ci-${{ github.ref }}
  cancel-in-progress: true

jobs:
  check:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-go@v5
        with:
          go-version-file: go.mod
      - run: go mod download
      - run: go vet ./...
      - run: go build ./...
      - name: Run tests
        run: go test ./...
        continue-on-error: true
CI_EOF
      ;;

    rust)
      cat > .github/workflows/ci.yml << 'CI_EOF'
name: CI

on:
  push:
    branches: [main]
  pull_request:
    branches: [main]

concurrency:
  group: ci-${{ github.ref }}
  cancel-in-progress: true

jobs:
  check:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: dtolnay/rust-toolchain@stable
        with:
          components: clippy
      - uses: Swatinem/rust-cache@v2
      - run: cargo clippy -- -D warnings
      - run: cargo build
      - name: Run tests
        run: cargo test
        continue-on-error: true
CI_EOF
      ;;
  esac

  echo "Created .github/workflows/ci.yml"
fi
# ─── Append agent instructions to CLAUDE.md ──────────────────────────
if [[ ! -f "CLAUDE.md" ]]; then
  echo "# $REPO_NAME" > CLAUDE.md
  echo "" >> CLAUDE.md
  echo "Created CLAUDE.md"
else
  echo "" >> CLAUDE.md
  echo "Appending agent instructions to existing CLAUDE.md"
fi

# Idempotent: skip if already appended
if grep -q "## Autonomous Loop Rules" CLAUDE.md 2>/dev/null; then
  echo "Agent instructions already present in CLAUDE.md, skipping"
else
  cat >> CLAUDE.md << CLAUDE_EOF

# $REPO_NAME — Agent Instructions

## Project
$(if [[ "$LANG_TYPE" == "node" && -n "$PKG_DIR" ]]; then
  echo "Node.js project. Package directory: \`$PKG_DIR/\`. Source: \`$SRC_DIR/\`. Package manager: \`$PKG_MGR\`."
elif [[ "$LANG_TYPE" == "python" ]]; then
  echo "Python project. Source: \`$SRC_DIR/\`."
elif [[ "$LANG_TYPE" == "go" ]]; then
  echo "Go project. Module: \`go.mod\`."
elif [[ "$LANG_TYPE" == "rust" ]]; then
  echo "Rust project. Source: \`$SRC_DIR/\`. Manifest: \`Cargo.toml\`."
else
  echo "Project detected at \`$REPO_ROOT\`."
fi)

## Commands
$(if [[ -n "$INSTALL_CMD" ]]; then echo "- Install: \`$INSTALL_CMD\`"; fi)
$(if [[ -n "$LINT_CMD" ]]; then echo "- Lint: \`$LINT_CMD\`"; fi)
$(if [[ -n "$BUILD_CMD" ]]; then echo "- Build: \`$BUILD_CMD\`"; fi)
$(if [[ -n "$TEST_CMD" ]]; then echo "- Test: \`$TEST_CMD\`"; fi)
$(if [[ -z "$INSTALL_CMD" && -z "$LINT_CMD" && -z "$BUILD_CMD" && -z "$TEST_CMD" ]]; then echo "No standard commands detected. Configure manually if needed."; fi)

## Git Conventions
- Conventional commits: \`feat:\`, \`fix:\`, \`chore:\`, \`test:\`, \`refactor:\`
- Feature branches: \`claude/<description>-<id>\`
- Always push with \`-u origin <branch>\`
- Never force push. Never merge/rebase main without human approval.

## Autonomous Loop Rules
- Use git worktrees for isolation: \`git worktree add /tmp/$REPO_NAME-<task> -b claude/<task>-<id>\`
- After every change: run lint > build > test (if available) > only commit if all pass
- One logical change per commit. Keep changes narrow and independent.
- If 3+ consecutive failures on the same issue, stop and report.
- Clean up worktrees when done: \`git worktree remove /tmp/$REPO_NAME-<task>\`
- **Maintenance dedup** (Lint Guardian, Build Watchdog only): before creating a new lint-fix or build-fix branch, check if an identical open PR already exists. If so, skip. Issue branches are always unique.

## Epic / Project Branch Rules
- If an issue describes a **large project** with multiple sub-tasks, create a parent epic branch: \`claude/epic-<name>\`
- Sub-task branches are created from the epic branch (not main): \`git worktree add /tmp/$REPO_NAME-<subtask> -b claude/<subtask> claude/epic-<name>\`
- Sub-task PRs target the epic branch as base (not main)
- When all sub-tasks are merged into the epic branch, create a single PR from epic branch > main for human review
- After breaking an epic into sub-tasks, add the \`claude-epic-done\` label to the epic issue (alongside the model label). This signals catch-up loops to skip it — the epic has been decomposed and should not be re-processed.
- Labels after epic breakdown: \`["claude-opus", "claude-epic-done"]\` (or \`["claude-epic-done"]\` in single mode)

## Agent Backlog Convention
$(if [[ "$MODEL_MODE" == "dual" ]]; then
cat << 'DUAL_BACKLOG'
- **GitHub Issues** with label `claude-ready` are the primary work queue.
- The **Triage Worker** automatically classifies issues as `claude-sonnet` or `claude-opus`.
- Skip triage by labeling issues `claude-sonnet` or `claude-opus` directly.
- Claim issues by: setting labels to ["claude-wip", model-label] in a single mcp__github__issue_write(method: "update", labels: [...]) call — this REPLACES all labels. Self-assign.
DUAL_BACKLOG
else
cat << 'SINGLE_BACKLOG'
- **GitHub Issues** with label `claude-ready` are the work queue.
- Claim issues by: setting labels to ["claude-wip"] in a single mcp__github__issue_write(method: "update", labels: ["claude-wip"]) call — this REPLACES all labels, removing claude-ready. Self-assign.
SINGLE_BACKLOG
fi)
- When done: create a PR with "Closes #N" in the body, comment on the issue with PR link, remove \`claude-wip\`.
- If blocked: add \`claude-blocked\` label, comment explaining the problem.
- **Inline TODOs**: \`// TODO(@claude): <description>\` in source files are micro-tasks.
- Regular \`// TODO:\` comments (without @claude) are NOT agent work items — leave them alone.
- When completing an inline TODO(@claude): remove the comment entirely after implementing.
- One item per iteration. Never pick up multiple items at once.
$(if [[ "$MODEL_MODE" == "dual" ]]; then
